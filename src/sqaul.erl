-module(sqaul).

%% ------------------------------------------------------------------
%% API Function Exports
%% ------------------------------------------------------------------

-export([select/1]).


%% ------------------------------------------------------------------
%% API Function Definitions
%% ------------------------------------------------------------------

select(Table) when is_atom(Table) -> select(atom_to_list(Table));
select(Table) when is_list(Table) -> <<"select * from ", (iolist_to_binary(Table))/binary>>.

